const CONTRACT_ADDRESS = "0x22332737F1Aa79639D06BDF2A6427B8D246D1684"; // Replace with your contract address

const App = () => {
  const [account, setAccount] = useState("");
  const [web3, setWeb3] = useState(null);
  const [contract, setContract] = useState(null);

  const connectWallet = async () => {
    if (window.ethereum) {
      const web3Instance = new Web3(window.ethereum);
      const accounts = await window.ethereum.request({
        method: "eth_requestAccounts",
      });
      const contractInstance = new web3Instance.eth.Contract(
        CONTRACT_ABI,
        CONTRACT_ADDRESS
      );
      setAccount(accounts[0]);
      setWeb3(web3Instance);
      setContract(contractInstance);
    } else {
      alert("Please install MetaMask to use this dApp!");
    }
  };

  const claimTokens = async () => {
    if (!web3 || !contract) {
      return alert("Please connect to MetaMask first!");
    }
    try {
      await contract.methods.claimTokens().send({ from: account });
      alert("Successfully claimed your tokens!");
    } catch (error) {
      console.error(error);
      alert("An error occurred while claiming tokens.");
    }
  };

  return (
    <div>
      <h1>Joshcoin dApp</h1>
      {account ? <p>Connected account: {account}</p> : null}
      <button onClick={connectWallet}>Connect Wallet</button>
      <button onClick={claimTokens}>Claim Joshcoin</button>
    </div>
  );
};

export default App;
