function App() {
  const [provider, setProvider] = useState(null);
  const [signer, setSigner] = useState(null);
  const [joshcoin, setJoshcoin] = useState(null);
  const [balance, setBalance] = useState(null);
  const [claimed, setClaimed] = useState(false);
  const [claimAmount, setClaimAmount] = useState(0);

  useEffect(() => {
    const init = async () => {
      if (window.ethereum) {
        const web3Provider = new ethers.providers.Web3Provider(window.ethereum);
        setProvider(web3Provider);

        const signer = web3Provider.getSigner();
        setSigner(signer);

        const joshcoinContract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
        setJoshcoin(joshcoinContract);
      }
    };

    init();
  }, []);

  const connectWallet = async () => {
    if (window.ethereum) {
      await window.ethereum.request({ method: "eth_requestAccounts" });
      const signer = provider.getSigner();
      setSigner(signer);
    }
  };

  const claimTokens = async () => {
    if (!joshcoin || !signer) return;

    const tx = await joshcoin.claimTokens(ethers.utils.parseEther(claimAmount.toString()));
    await tx.wait();
    setClaimed(true);

    const signerAddress = await signer.getAddress();
    const updatedBalance = await joshcoin.balanceOf(signerAddress);
    setBalance(updatedBalance);
  };

  const checkBalance = async () => {
    if (!joshcoin || !signer) return;

    const signerAddress = await signer.getAddress();
    const balance = await joshcoin.balanceOf(signerAddress);
    setBalance(balance);
  };

  return (
    <div className="App">
      <h1>Joshcoin</h1>
      <button onClick={connectWallet}>Connect Wallet</button>
      <button onClick={checkBalance}>Check Balance</button>
      {balance !== null && <p>Balance: {ethers.utils.formatEther(balance.toString())} JC</p>}
      {!claimed && (
        <>
          <input
            type="number"
            value={claimAmount}
            onChange={(e) => setClaimAmount(e.target.value)}
             min="1"
            max="100"
          />
          <button onClick={claimTokens}>Claim Tokens</button>
        </>
      )}
      {claimed && <p>Successfully claimed {claimAmount} JC!</p>}
    </div>
  );
}

export default App;